public class StampData {
    private String mStampTitle;
    private int mStampCounter;
    private int mStampIcon;

    public String getmStampTitle() {
        return mStampTitle;
    }

    public void setmStampTitle(String mStampTitle) {
        this.mStampTitle = mStampTitle;
    }

    public int getmStampCounter() {
        return mStampCounter;
    }

    public void setmStampCounter(int mStampCounter) {
        this.mStampCounter = mStampCounter;
    }

    public int getmStampIcon() {
        return mStampIcon;
    }

    public void setmStampIcon(int mStampIcon) {
        this.mStampIcon = mStampIcon;
    }
}
